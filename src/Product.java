
public class Product {
	 private String productName;
	    private String date;

	    public Product(String productName, String date) {
	        this.productName = productName;
	        this.date = date;
	    }

	    public String getProductName() {
	        return productName;
	    }

	    public String getDate() {
	        return date;
	    }
}
